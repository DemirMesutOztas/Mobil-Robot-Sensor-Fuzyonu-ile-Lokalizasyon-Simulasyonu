def generate_square_commands(side=10.0, v=1.0, dt=0.1):
    commands = []

    n_straight = int(side / v / dt)    # 10 / 1.0 / 0.1 = 100 adım

    t_turn  = 1.5                      # 1.5 saniyede 90 derece
    omega   = (np.pi / 2) / t_turn    # ≈ 1.047 rad/s
    n_turn  = int(t_turn / dt)         # 1.5 / 0.1 = 15 adım

    for _ in range(4):
        for _ in range(n_straight):
            commands.append((v, 0.0))
        for _ in range(n_turn):
            commands.append((0.0, omega))

    return commands

def run_simulation(dt=0.1, side=10.0):

    Q = np.diag([0.01**2, 0.01**2, 0.005**2])   # sistem gürültüsü
    R = np.diag([0.05**2, 0.05**2])              # LIDAR ölçüm gürültüsü

    robot    = DifferentialRobot()
    odom     = OdometrySensor(sigma_v=0.05, sigma_omega=0.02)
    lidar    = LidarSensor(sigma_x=0.05, sigma_y=0.05)
    ekf      = EKF(init_state=np.zeros(3), Q=Q, R=R)
    commands = generate_square_commands(side=side, v=1.0, dt=dt)

    true_traj = [np.zeros(3)]    # gerçek yörünge
    odom_traj = [np.zeros(3)]    # odometri tahmini
    ekf_traj  = [np.zeros(3)]    # EKF tahmini
    times     = [0.0]            # zaman

    for i, (v, omega) in enumerate(commands):
        t = (i + 1) * dt

        true_state = robot.move(v, omega, dt)

        odom_state = odom.update(v, omega, dt)

        ekf.predict(v, omega, dt)

        if i % 5 == 0:
            z = lidar.measure(true_state)
            ekf.update(z)

        true_traj.append(true_state.copy())
        odom_traj.append(odom_state.copy())
        ekf_traj.append(ekf.x.copy())
        times.append(t)

    return (np.array(true_traj),
            np.array(odom_traj),
            np.array(ekf_traj),
            np.array(times))

def plot_results(true_traj, odom_traj, ekf_traj, times):

    #Hata
    err_odom = np.linalg.norm(true_traj[:, :2] - odom_traj[:, :2], axis=1)
    err_ekf  = np.linalg.norm(true_traj[:, :2] - ekf_traj[:, :2],  axis=1)

    #RMSE
    rmse_odom = np.sqrt(np.mean(err_odom**2))
    rmse_ekf  = np.sqrt(np.mean(err_ekf**2))
    #Yörünge
    fig,ax = plt.subplots(figsize=(8, 8))

    ax.plot(true_traj[:, 0], true_traj[:, 1],
            color='blue', lw=2.5, label='Gerçek Yörünge')
    ax.plot(odom_traj[:, 0], odom_traj[:, 1],
            color='red', lw=1.5, linestyle='--',
            label=f'Odometri (RMSE={rmse_odom:.3f} m)')
    ax.plot(ekf_traj[:, 0], ekf_traj[:, 1],
            color='green', lw=1.8, linestyle='-.',
            label=f'EKF Füzyonu (RMSE={rmse_ekf:.3f} m)')
    ax.scatter([0], [0], color='black', s=100, zorder=5, marker='*')
    ax.annotate('Başlangıç', (0, 0), xytext=(0.3, 0.4), fontsize=9)
    ax.set_xlabel('x (m)')
    ax.set_ylabel('y (m)')
    ax.set_title('Yörünge Karşılaştırması')
    ax.legend()
    ax.set_aspect('equal')
    ax.grid(True, alpha=0.3)
    fig.tight_layout()
    print("\n" * 3)

    #Konum hatası-zaman
    fig2,ax = plt.subplots(figsize=(8, 8))
    ax.plot(times, err_odom, color='red', lw=1.4,
            label=f'Odometri  RMSE = {rmse_odom:.3f} m')
    ax.fill_between(times, err_odom, alpha=0.15, color='red')
    ax.plot(times, err_ekf, color='green', lw=1.6,
            label=f'EKF Füzyon RMSE = {rmse_ekf:.3f} m')
    ax.fill_between(times, err_ekf, alpha=0.15, color='green')
    ax.set_xlabel('Zaman (s)')
    ax.set_ylabel('Konum Hatası (m)')
    ax.set_title('Lokalizasyon Hatası — Zaman')
    ax.legend()
    ax.set_ylim(bottom=0)
    ax.grid(True, alpha=0.3)

    fig2.tight_layout()
    #plt.tight_layout()
    plt.savefig('simulasyon.png', dpi=150, bbox_inches='tight')
    plt.show()

    print(f"  Odometri RMSE  : {rmse_odom:.4f} m")
    print(f"  EKF RMSE       : {rmse_ekf:.4f} m")
    print(f"  İyileşme Oranı : %{(1 - rmse_ekf/rmse_odom)*100:.1f}")

if __name__ == "__main__":
    true_traj, odom_traj, ekf_traj, times = run_simulation(dt=0.1, side=10.0)
    plot_results(true_traj, odom_traj, ekf_traj, times)