import numpy as np
import matplotlib.pyplot as plt

class DifferentialRobot:

    def __init__(self, x0=0.0, y0=0.0, theta0=0.0):

        self.state = np.array([x0, y0, theta0])

    def move(self, v, omega, dt):
        """
        Robotu hareket ettirir.
        v     : doğrusal hız (m/s)
        omega : açısal hız (rad/s)
        dt    : zaman adımı (s)
        """
        x, y, theta = self.state

        self.state[0] += v * np.cos(theta) * dt   # x yönünde hareket
        self.state[1] += v * np.sin(theta) * dt   # y yönünde hareket
        self.state[2] += omega * dt                # açı değişimi
        self.state[2] = (self.state[2] + np.pi) % (2 * np.pi) - np.pi

        return self.state.copy()

class OdometrySensor:

    def __init__(self, sigma_v=0.05, sigma_omega=0.02, rng_seed=42):
        self.sigma_v     = sigma_v        # hız gürültüsü
        self.sigma_omega = sigma_omega    # dönüş gürültüsü
        self.rng         = np.random.default_rng(rng_seed)
        self.state       = np.zeros(3)   # başlangıç0, 0, 0]

    def update(self, v, omega, dt):
        v_gurultulu     = v     + self.rng.normal(0, self.sigma_v)
        omega_gurultulu = omega + self.rng.normal(0, self.sigma_omega)
        x, y, theta = self.state

        self.state[0] += v_gurultulu     * np.cos(theta) * dt
        self.state[1] += v_gurultulu     * np.sin(theta) * dt
        self.state[2] += omega_gurultulu * dt
        self.state[2] = (self.state[2] + np.pi) % (2 * np.pi) - np.pi

        return self.state.copy()

class LidarSensor:

    def __init__(self, sigma_x=0.05, sigma_y=0.05, rng_seed=7):
        self.sigma_x = sigma_x    # x ölçüm gürültüsü
        self.sigma_y = sigma_y    # y ölçüm gürültüsü
        self.R       = np.diag([sigma_x**2, sigma_y**2])  # ölçüm kovaryansı
        self.rng     = np.random.default_rng(rng_seed)

    def measure(self, true_state):
        z = np.array([
            true_state[0] + self.rng.normal(0, self.sigma_x),  # x ölçümü
            true_state[1] + self.rng.normal(0, self.sigma_y)   # y ölçümü
        ])
        return z

class EKF:

    def __init__(self, init_state, Q, R):
        self.x = init_state.copy()   # başlangıç durum tahmini
        self.P = np.eye(3) * 0.1     # başlangıç belirsizlik matrisi
        self.Q = Q                   # sistem gürültüsü kovaryansı
        self.R = R                   # ölçüm gürültüsü kovaryansı

    def predict(self, v, omega, dt):
        x, y, theta = self.x

        # Tahmin
        self.x[0] += v * np.cos(theta) * dt
        self.x[1] += v * np.sin(theta) * dt
        self.x[2] += omega * dt
        self.x[2]  = (self.x[2] + np.pi) % (2 * np.pi) - np.pi

        #Jacobian matrisi F
        F = np.array([
            [1, 0, -v * np.sin(theta) * dt],
            [0, 1,  v * np.cos(theta) * dt],
            [0, 0,  1                     ]
        ])

        #Kovaryans tahmini
        # P⁻ = F · P · Fᵀ + Q
        self.P = F @ self.P @ F.T + self.Q

    def update(self, z):
        H = np.array([
            [1, 0, 0],
            [0, 1, 0]
        ])

        # S = H · P⁻ · Hᵀ + R
        S = H @ self.P @ H.T + self.R

        # K = P⁻ · Hᵀ · S⁻¹
        K = self.P @ H.T @ np.linalg.inv(S)

        innovation = z - H @ self.x

        # x̂ = x̂⁻ + K · inovasyon
        self.x = self.x + K @ innovation

        # P = (I - K·H) · P⁻
        I      = np.eye(3)
        self.P = (I - K @ H) @ self.P

